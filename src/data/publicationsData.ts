/** Path to the publications index. */
export function publicationsListPath(
  basePath: string,
  page: number,
  category: PublicationCategory,
): string {
  const params = new URLSearchParams();
  const first = publicationCategories[0].id;
  if (page > 1) params.set("page", String(page));
  if (category !== first) params.set("cat", category);
  const qs = params.toString();
  return `${basePath}/publications${qs ? `?${qs}` : ""}`;
}

